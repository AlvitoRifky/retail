    class penjualan extends transaksi{
        int id_transaksi, id_produk, harga, qty, total_bayar;
        String nama_pelanggan, tanggal, nama_produk;
        public penjualan() {
            this.nama_pelanggan = fnama_pelanggan.getText();
            String combo = fid_produk.getSelectedItem().toString();
            String[] arr = combo.split(":");
            this.id_produk = Integer.parseInt(arr[0]);
            try {
                Date date = ftanggal.getDate();
                DateFormat dateformat = new SimpleDateFormat("YYYY-MM-dd");
                this.tanggal = dateformat.format(date);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            this.nama_produk = arr[1];
            this.harga = Integer.parseInt(arr[2]);
            this.qty = Integer.parseInt(fqty.getText());
            this.total_bayar = this.harga * this.qty;
        }
    }
    
    public void refreshTabel(){
        model = new DefaultTableModel();
        model.addColumn("ID Transaksi");
        model.addColumn("Nama Pelanggan");
        model.addColumn("ID Produk");
        model.addColumn("Tanggal");
        model.addColumn("Nama Produk");
        model.addColumn("Harga");
        model.addColumn("Qty");
        model.addColumn("Total Bayar");
        ttransaksi.setModel(model);
        try {
            this.stat = cn.prepareStatement("select * from transaksi");
            this.rs = this.stat.executeQuery();
            while (rs.next()){
                Object[] data={
                  rs.getString(1),
                  rs.getString(2),
                  rs.getString(3),
                  rs.getString(4),
                  rs.getString(5),
                  rs.getString(6),
                  rs.getString(7),
                  rs.getString(8)
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        fid_transaksi.setText("");
        fnama_pelanggan.setText("");
        fqty.setText("");
        ftotal.setText("");
        
    }
    
    public void refreshCombo(){
        try {
            this.stat = cn.prepareStatement("select * from produk");
            this.rs = this.stat.executeQuery();
             while (rs.next()){
                 fid_produk.addItem(rs.getString("id_produk")+":"+rs.getString("nama_produk")+":"+rs.getString("harga"));
             }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

