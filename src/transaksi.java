    private void bupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bupdateActionPerformed
        try {
             penjualan tran = new penjualan();
             tran.id_transaksi = Integer.parseInt(fid_transaksi.getText());
             this.stat = cn.prepareStatement("update transaksi set nama_pelanggan=?,"+ "id_produk=?,tanggal=?,nama_produk=?,harga=?,"
                     + "qty=?,total_bayar=? where id_transaksi=?");
             this.stat.setString(1, tran.nama_pelanggan);
             this.stat.setInt(2, tran.id_produk);
             this.stat.setString(3, tran.tanggal);
             this.stat.setString(4, tran.nama_produk);
             this.stat.setInt(5, tran.harga);
             this.stat.setInt(6, tran.qty);
             this.stat.setInt(7, tran.total_bayar);
             this.stat.setInt(8, Integer.parseInt(fid_transaksi.getText()));
             this.stat.executeUpdate();
             refreshTabel();
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_bupdateActionPerformed

